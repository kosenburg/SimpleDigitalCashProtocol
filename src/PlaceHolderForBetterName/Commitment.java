package PlaceHolderForBetterName;

public class Commitment {
    private long leftCommit;
    private long rightCommit;

    public Commitment() {
        this.leftCommit = leftCommit;
        this.rightCommit = rightCommit;

    }

    public long getLeftCommit() {
        return leftCommit;
    }

    public void setLeftCommit(long leftCommit) {
        this.leftCommit = leftCommit;
    }

    public void setRightCommit(long rightCommit) {
        this.rightCommit = rightCommit;
    }

    public long getRightCommit() {
        return rightCommit;
    }
}
